""" data_types.py
Python Program to Demonstrate Data Types and Variables
"""

# Integer Data Type
# Here, we assign an integer value to the variable 'integer_number'
integer_number = 42
print("Integer Example:", integer_number)  # Output will be 42

# Float Data Type
# Assigning a floating-point value to 'float_number'
float_number = 3.14159
print("Float Example:", float_number)  # Output will be 3.14159

# String Data Type
# Strings can be defined with either single or double quotes
simple_string = "Hello, Python!"
print("String Example:", simple_string)  # Output will be "Hello, Python!"

# Boolean Data Type
# Booleans represent True or False values
is_learning_fun = True
print("Boolean Example:", is_learning_fun)  # Output will be True

# List Data Type
# Lists are used to store multiple items in a single variable
number_list = [1, 2, 3, 4, 5]
print("List Example:", number_list)  # Output will be [1, 2, 3, 4, 5]

# Dictionary Data Type
# Dictionaries are used to store data values in key:value pairs
person = {"name": "Alice", "age": 25}
print("Dictionary Example:", person)  # Output will be {'name': 'Alice', 'age': 25}

# Tuple Data Type
# A tuple is a collection which is ordered and unchangeable
color_tuple = ("red", "green", "blue")
print("Tuple Example:", color_tuple)  # Output will be ('red', 'green', 'blue')

# Set Data Type
# Sets are used to store multiple items in a single variable, but unlike lists, they are unordered and unindexed
fruit_set = {"apple", "banana", "cherry"}
print("Set Example:", fruit_set)
